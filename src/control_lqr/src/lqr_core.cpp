// ============================================================
// init() — Q, R 행렬 초기화
//
// [호출 시점]
//   lqr_node 생성자에서 단 한 번 호출됨.
//   ROS2 파라미터로 받은 가중치 값들을 실제 행렬로 조립.
//
// [Q 행렬이란?]
//   LQR의 비용함수는 J = Σ (e^T Q e + u^T R u) 로 정의됨.
//   Q는 "상태 오차를 얼마나 엄격하게 줄일 것인가"를 결정함.
//   Q(i,i)가 클수록 i번째 상태 오차를 더 빠르게 0으로 만들려 함.
//
//   예시: q_x=10, q_th=5 이면
//     x 위치가 1m 틀렸을 때 비용 = 10×1² = 10
//     θ가 1rad 틀렸을 때 비용 = 5×1² = 5
//     → LQR이 위치 오차를 heading 오차보다 2배 더 중요하게 취급
//
// [R 행렬이란?]
//   R은 "입력(제어 명령)을 얼마나 아낄 것인가"를 결정함.
//   R(i,i)가 클수록 해당 입력의 변화량을 억제함 → smooth 제어
//   R(i,i)가 작을수록 공격적으로 오차를 줄임 → 진동 위험
//
//   Q와 R은 절대값이 아닌 비율이 중요함:
//     Q/R 비율이 크면  → K 커짐 (공격적, 빠른 수렴, 진동 가능)
//     Q/R 비율이 작으면 → K 작아짐 (온순함, 느린 수렴, smooth)
// ============================================================
void LqrCore::init(const LqrParams & params)
{
  params_ = params;

  // --------------------------------------------------------
  // Q: 상태 추적 비용 행렬 (5×5 대각 행렬)
  //
  //   대각 행렬로 구성하는 이유:
  //   각 상태 오차가 독립적으로 패널티를 받도록 설계.
  //   비대각 원소가 0이 아니면 두 상태 오차 간 교차 패널티가 생겨
  //   설계가 복잡해지고 직관성이 떨어짐.
  //
  //   Q = diag(q_x, q_y, q_th, q_v, q_w)
  //
  //       [q_x  0    0    0    0  ]   x   위치 오차 패널티
  //       [0    q_y  0    0    0  ]   y   위치 오차 패널티
  //       [0    0    q_th 0    0  ]   θ   heading 오차 패널티
  //       [0    0    0    q_v  0  ]   v   선속도 오차 패널티
  //       [0    0    0    0    q_w]   ω   각속도 오차 패널티
  // --------------------------------------------------------
  Q_ = LqrQMat::Zero();
  Q_(0, 0) = params_.q_x;   // x  위치 오차 패널티
  Q_(1, 1) = params_.q_y;   // y  위치 오차 패널티
  Q_(2, 2) = params_.q_th;  // θ heading 오차 패널티
  Q_(3, 3) = params_.q_v;   // v  선속도 오차 패널티
  Q_(4, 4) = params_.q_w;   // ω  각속도 오차 패널티

  // --------------------------------------------------------
  // R: 입력 비용 행렬 (2×2 대각 행렬)
  //
  //   입력 벡터 u = [Δv, Δω]이므로 2×2 행렬.
  //
  //   R = diag(r_dv, r_dw)
  //
  //       [r_dv  0   ]   Δv 패널티 (선속도 변화 억제)
  //       [0     r_dw]   Δω 패널티 (각속도 변화 억제)
  //
  //   r_dw를 크게 하면 ω 변화가 억제되어 회전이 부드러워짐.
  //   이 프로젝트에서 LQR의 Δω std가 MPC보다 4.4배 큰 이유 중 하나는
  //   MPC는 r_dw가 입력 시퀀스 전체에 적용되는 반면
  //   LQR은 단일 스텝에만 적용되기 때문임.
  // --------------------------------------------------------
  R_ = LqrRMat::Zero();
  R_(0, 0) = params_.r_dv;  // Δv 패널티 (선속도 변화 억제)
  R_(1, 1) = params_.r_dw;  // Δω 패널티 (각속도 변화 억제)

  // warm-start 상태 초기화
  // is_p_initialized_=false: 첫 compute() 호출 시 P=Q로 cold-start하도록 설정
  is_p_initialized_ = false;
}

// ============================================================
// linearize() — 현재 동작점에서 연속시간 Jacobian 선형화
//
// [왜 선형화가 필요한가?]
//   Diff-Drive AMR의 실제 운동 방정식은 비선형(Nonlinear)임:
//     ẋ = v·cos(θ)    ← cos(θ) 때문에 비선형
//     ẏ = v·sin(θ)    ← sin(θ) 때문에 비선형
//     θ̇ = ω
//     v̇ = Δv/dt
//     ω̇ = Δω/dt
//
//   LQR은 선형 시스템(ẋ = Ax + Bu)에만 적용 가능.
//   → 현재 동작점(x_bar) 주변에서 1차 Taylor 전개:
//     f(x) ≈ f(x_bar) + ∂f/∂x|_{x_bar} · (x - x_bar)
//                              ↑ 이게 바로 A_c (Jacobian)
//
// [Jacobian이란?]
//   f: R^n → R^m 인 벡터 함수의 모든 편미분을 모아 놓은 행렬.
//   A_c(i,j) = ∂fᵢ/∂xⱼ
//   즉, j번째 상태가 조금 변할 때 i번째 상태 변화율이 얼마나 바뀌는가.
//
// [동작점으로 x_ref를 쓰는 이유]
//   x0(현재 실제 상태) 대신 x_ref(목표 상태)를 쓰는 이유:
//   → 추적 오차(x0 - x_ref)가 작을수록 x_ref 근방에서의 선형화가
//     실제 시스템 동작을 더 정확하게 근사함.
//   → 오차가 0으로 수렴할수록 선형화 오차도 0으로 수렴 (이상적).
//
// [A_c 유도 과정 상세]
//   f = [v·cos(θ),  v·sin(θ),  ω,  Δv/dt,  Δω/dt]^T
//   상태 x = [x_pos, y_pos, θ, v, ω]^T
//
//   ∂f₀/∂x = [0, 0, ∂(v·cos θ)/∂θ, ∂(v·cos θ)/∂v, 0]
//           = [0, 0, -v·sin(θ),       cos(θ),          0]
//
//   ∂f₁/∂x = [0, 0, ∂(v·sin θ)/∂θ, ∂(v·sin θ)/∂v, 0]
//           = [0, 0,  v·cos(θ),       sin(θ),          0]
//
//   ∂f₂/∂x = [0, 0, 0, 0, ∂ω/∂ω] = [0, 0, 0, 0, 1]
//
//   ∂f₃/∂x = [0, 0, 0, 0, 0]   (Δv/dt는 상태에 무관)
//   ∂f₄/∂x = [0, 0, 0, 0, 0]   (Δω/dt는 상태에 무관)
//
// [B_c 유도 과정 상세]
//   입력 u = [Δv, Δω]^T
//
//   ∂f₃/∂u = [∂(Δv/dt)/∂(Δv), ∂(Δv/dt)/∂(Δω)] = [1/dt, 0]
//   ∂f₄/∂u = [∂(Δω/dt)/∂(Δv), ∂(Δω/dt)/∂(Δω)] = [0, 1/dt]
//   나머지 행은 모두 0 (x_pos, y_pos, θ는 입력에 직접 의존 안 함)
// ============================================================
void LqrCore::linearize(
  const LqrStateVec & x_bar,
  LqrStateMat & A_c,
  LqrInputMat & B_c) const
{
  // 동작점에서 θ, v 추출
  // x_bar = [x_pos, y_pos, θ, v, ω]^T
  const double theta = x_bar(2);  // 현재 heading [rad]
  const double v     = x_bar(3);  // 현재 선속도  [m/s]

  // --------------------------------------------------------
  // A_c = ∂f/∂x (5×5 연속시간 상태 행렬)
  //
  //       col:  x_pos  y_pos    θ              v          ω
  //   row f₀:  [  0,     0,  -v·sin(θ),     cos(θ),      0  ]
  //   row f₁:  [  0,     0,   v·cos(θ),     sin(θ),      0  ]
  //   row f₂:  [  0,     0,      0,             0,        1  ]
  //   row f₃:  [  0,     0,      0,             0,        0  ]
  //   row f₄:  [  0,     0,      0,             0,        0  ]
  //
  //   0이 아닌 원소만 설정하고 나머지는 Zero() 초기화로 처리.
  // --------------------------------------------------------
  A_c = LqrStateMat::Zero();

  // [행 0: ẋ_pos = v·cos(θ)]
  // θ에 대한 편미분: ∂(v·cos θ)/∂θ = -v·sin(θ)
  // v에 대한 편미분: ∂(v·cos θ)/∂v =  cos(θ)
  A_c(0, 2) = -v * std::sin(theta);  // ∂ẋ/∂θ
  A_c(0, 3) =  std::cos(theta);      // ∂ẋ/∂v

  // [행 1: ẏ_pos = v·sin(θ)]
  // θ에 대한 편미분: ∂(v·sin θ)/∂θ =  v·cos(θ)
  // v에 대한 편미분: ∂(v·sin θ)/∂v =  sin(θ)
  A_c(1, 2) =  v * std::cos(theta);  // ∂ẏ/∂θ
  A_c(1, 3) =  std::sin(theta);      // ∂ẏ/∂v

  // [행 2: θ̇ = ω]
  // ω에 대한 편미분: ∂ω/∂ω = 1
  A_c(2, 4) = 1.0;  // ∂θ̇/∂ω

  // [행 3, 4]
  // v̇ = Δv/dt, ω̇ = Δω/dt 는 상태 x에 의존하지 않으므로
  // A_c의 3행, 4행은 모두 0 → Zero() 초기화로 이미 처리됨

  // --------------------------------------------------------
  // B_c = ∂f/∂u (5×2 연속시간 입력 행렬)
  //
  //   입력 u = [Δv, Δω]^T
  //
  //       col:  Δv       Δω
  //   row f₀:  [ 0,       0  ]   x_pos는 입력에 직접 무관
  //   row f₁:  [ 0,       0  ]   y_pos는 입력에 직접 무관
  //   row f₂:  [ 0,       0  ]   θ는 입력에 직접 무관
  //   row f₃:  [1/dt,     0  ]   v̇ = Δv/dt → ∂v̇/∂(Δv) = 1/dt
  //   row f₄:  [ 0,     1/dt ]   ω̇ = Δω/dt → ∂ω̇/∂(Δω) = 1/dt
  //
  //   [왜 1/dt가 나오는가?]
  //   입력을 Δv (속도 변화량)로 정의했기 때문.
  //   v̇ = Δv/dt 이므로 ∂v̇/∂(Δv) = 1/dt
  //   이산화 후에는 B_d = B_c·dt → B_d(3,0) = 1 이 됨 (1/dt · dt = 1)
  // --------------------------------------------------------
  B_c = LqrInputMat::Zero();

  B_c(3, 0) = 1.0 / params_.dt;  // Δv → 선속도 증분율
  B_c(4, 1) = 1.0 / params_.dt;  // Δω → 각속도 증분율
}

// ============================================================
// 반복식 (Discrete-time Algebraic Riccati Equation, DARE):
// P_{new} = Q + A^T P A - A^T P B (R + B^T P B)^{-1} B^T P A
// Continuous Algebraric Riccati Equation, CARE의 경우엔
// A^T P + P A − P B R^−1 B^T P + Q = 0

// discretize() — Euler 1차 이산화
//
// [왜 이산화가 필요한가?]
//   linearize()에서 얻은 A_c, B_c는 연속시간 모델:
//     ẋ(t) = A_c·x(t) + B_c·u(t)  (미분 방정식)
//
//   컴퓨터는 연속시간을 직접 다룰 수 없으므로,
//   dt=0.02s 주기의 이산시간 모델로 변환:
//     x[k+1] = A_d·x[k] + B_d·u[k]  (차분 방정식)
//
// [Euler 1차 근사란?]
//   정확한 이산화 방법은 ZOH(Zero-Order Hold):
//     A_d = e^{A_c·dt}  (행렬 지수함수)
//     B_d = (e^{A_c·dt} - I)·A_c^{-1}·B_c
//
//   하지만 행렬 지수함수 계산은 무겁고, dt가 작으면
//   1차 Taylor 근사로 충분함:
//     e^{A_c·dt} ≈ I + A_c·dt  (dt²이상 항 생략)
//
//   → A_d = I + A_c·dt
//   → B_d = B_c·dt
//
// [Euler 이산화의 오차는?]
//   오차 = O(dt²) = O(0.02²) = O(0.0004)
//   v=0.1m/s, dt=0.02s → 한 스텝 이동 거리=0.002m
//   이 수준에서 Euler 오차는 실용적으로 무시 가능.
//   또한 TVLQR은 매 주기 선형화를 다시 하므로 오차가 누적되지 않음.
//
// [이산화 결과 확인]
//   B_c(3,0) = 1/dt → B_d(3,0) = (1/dt)·dt = 1
//   B_c(4,1) = 1/dt → B_d(4,1) = (1/dt)·dt = 1
//   → "입력 Δv를 그대로 속도에 더한다"는 직관과 일치
// ============================================================
void LqrCore::discretize(
  const LqrStateMat & A_c,
  const LqrInputMat & B_c,
  LqrStateMat & A_d,
  LqrInputMat & B_d) const
{
  // Euler 1차 근사: A_d = I + A_c·dt
  // LqrStateMat::Identity()는 5×5 단위행렬(I)
  A_d = LqrStateMat::Identity() + A_c * params_.dt;

  // B_d = B_c·dt
  B_d = B_c * params_.dt;
}

// ============================================================
// solveRiccati() — 이산시간 Backward Riccati 반복 (Warm-start 포함)
//
// [DARE란? — Discrete-time Algebraic Riccati Equation]
//   이산시간 LQR에서 최적 비용 행렬 P를 구하는 방정식:
//
//   P = Q + A^T P A - A^T P B (R + B^T P B)^{-1} B^T P A
//
//   이 방정식의 해 P가 구해지면, 최적 게인을 계산할 수 있음:
//   K = (R + B^T P B)^{-1} B^T P A
//
// [왜 P를 구해야 하는가?]
//   LQR 비용함수 J = Σ_{k=0}^∞ (e_k^T Q e_k + u_k^T R u_k)를
//   최소화하는 최적 제어 u* = -K·e 를 구하는 과정에서
//   "무한히 먼 미래까지의 누적 비용"을 P 하나의 행렬로 압축함.
//   P(i,j)는 현재 상태 오차 i와 j 사이의 누적 비용 기여도.
//
// DARE를 푸는 두 가지 길
//
// 해밀토니안 행렬 H (10×10) 구성
//          ↓
//     Schur 분해
//     (고유값, 불변 부분 공간)
//          ↓
//     P = X₂ · X₁⁻¹  ← 1회 계산, 정확
//     (scipy/MATLAB이 하는 방식)
//
//          vs
//
// 초기값 P₀ = Q (or P_prev)
//          ↓
//     DARE 반복식 적용
//     P_{n+1} = Q + AᵀPA - AᵀPB(R+BᵀPB)⁻¹BᵀPA
//          ↓
//     ||P_new - P||_F < tol?
//     No  → 반복                  ← 코드가 하는 방식
//     Yes → 수렴, P 반환
//
// 왜 해밀토니안 대신 반복법을 쓰는가?
// Schur 분해법 (해밀토니안)	              반복법 (코드 구현)
// 정확도:	     수치적으로 정확	           수렴 허용 오차(tol) 내
// 계산 비용:	    10×10 고유값 분해	         5×5 행렬 곱 반복
// 구현 복잡도:	  매우 복잡 (라이브러리 필요)   단순한 반복문
// TVLQR 적합성: 매 스텝 재계산 비용 큼	      Warm Start로 빠르게 수렴
// 실시간 적합성:	 낮음	                    높음
// 50Hz 실시간 제어에서는 Schur 분해의 정확도보다 반복법의 속도가 더 중요, Warm Start 덕분에 반복 횟수가 매우 적어서 실용적
//
// [Backward Iteration이란?]
//   DARE를 직접 푸는 대신, 반복법으로 수렴시킴:
//     P₀ = Q (초기값: 비용행렬 = 상태 오차 비용)
//     P₁ = Q + A^T P₀ A - A^T P₀ B (R + B^T P₀ B)^{-1} B^T P₀ A
//     P₂ = Q + A^T P₁ A - ...
//     ...
//     P_∞ → P* (수렴)
//
//   이 반복식의 물리적 의미:
//   "k스텝 후의 비용을 현재 비용으로 환산(Bellman 방정식)"
//
// [Warm-start 원리]
//   TVLQR에서 인접한 두 제어 주기(20ms 간격)의 x_ref는
//   v=0.1m/s·0.02s = 0.002m밖에 차이 안 남.
//   → A_d, B_d의 변화량도 극히 작음
//   → 이전 P_prev ≈ 현재 P* 이미 성립
//   → P_prev에서 시작하면 수렴 토크(tol=1e-3)까지 수 회만에 도달
//
//   cold-start(P=Q에서 시작)의 문제:
//   Diff-Drive A_d의 고유값이 모두 1.0(marginal stable)이라
//   O(1/n)의 매우 느린 수렴 속도를 가짐 → 수천 회 필요
//
//   warm-start 효과(실험으로 확인):
//   cold-start: ~9ms (500iter 전부 소모)
//   warm-start:  0.08ms (수 회만에 수렴)
//
// [반환값]
//   true : tol 이내로 수렴 성공 → P_out이 최적해에 충분히 가까움
//   false: 최대 반복 초과 → P_out은 현재까지 수렴한 근사값
//          (경로 수신 직후 cold-start 시 발생, 제어는 정상 동작)
// ============================================================
bool LqrCore::solveRiccati(
  const LqrStateMat & A_d,
  const LqrInputMat & B_d,
  LqrStateMat & P_out)
{
  // --------------------------------------------------------
  // Warm-start 초기값 설정
  //
  //   is_p_initialized_ = false: 첫 호출 → P = Q (cold-start)
  //   is_p_initialized_ = true:  이후 호출 → P = P_prev_ (warm-start)
  //
  //   P = Q로 시작하는 이유(cold-start 시):
  //   "미래 비용을 모르는 상태에서 가장 보수적인 추정:
  //    현재 상태 오차 비용만 = Q"
  // --------------------------------------------------------
  LqrStateMat P = is_p_initialized_ ? P_prev_ : Q_;
  // 초기값 결정, 첫 호출이면 Q_로 콜드 스타트, 이후엔 직전 P로 시작(Warm Start)

  for (int i = 0; i < params_.riccati_iter; ++i) {
    // --------------------------------------------------------
    // Riccati 반복식의 핵심 연산
    //
    //   P_new = Q + A^T P A - A^T P B (R + B^T P B)^{-1} B^T P A
    //
    //   각 중간 행렬의 의미:
    //   BtP  = B^T · P        (2×5)
    //          "입력이 미래 비용에 미치는 영향"
    //
    //   S    = R + B^T P B    (2×2)
    //          "입력 비용 + 입력을 통한 미래 비용"
    //          이 행렬의 역행렬이 최적 게인의 핵심 분모
    //
    //   AtP  = A^T · P        (5×5)
    //          "상태 전이가 미래 비용에 미치는 영향"
    //
    //   P_new의 세 항 해석:
    //   ① Q                        : 현재 스텝 상태 오차 비용
    //   ② + A^T P A                : 다음 스텝으로 전파되는 비용
    //   ③ - A^T P B S^{-1} B^T P A : 최적 입력으로 줄일 수 있는 비용
    //   → Bellman 최적성 원리: "현재 비용 + 미래 비용 - 최적 절감분"
    // --------------------------------------------------------

    // BtP = B_d^T × P  (2×5)
    Eigen::Matrix<double, LQR_NU, LQR_NX> BtP = B_d.transpose() * P;

    // S = R + B^T P B  (2×2)
    // 2×2 행렬이므로 Eigen이 해석적으로 역행렬 계산 → 매우 빠름
    Eigen::Matrix<double, LQR_NU, LQR_NU> S = R_ + BtP * B_d;
    Eigen::Matrix<double, LQR_NU, LQR_NU> S_inv = S.inverse();

    // AtP = A_d^T × P  (5×5)
    LqrStateMat AtP = A_d.transpose() * P;

    // Riccati 반복식:
    // P_new = Q + A^T P A - A^T P B · S^{-1} · B^T P A
    //                        ↑ 이 항이 최적 입력으로 줄이는 비용
    LqrStateMat P_new = Q_ + AtP * A_d - AtP * B_d * S_inv * BtP * A_d;

    // --------------------------------------------------------
    // 수렴 판정: Frobenius norm
    //
    //   ||P_new - P||_F = sqrt(Σᵢⱼ (P_new(i,j) - P(i,j))²)
    //
    //   5×5 행렬의 25개 원소 변화량의 RMS값.
    //   이 값이 riccati_tol(=1e-3) 이하면 수렴으로 판정.
    //
    //   warm-start 시: 이미 P ≈ P*이므로 P_new - P가 매우 작음
    //   → 1~5회 반복만에 수렴 (약 0.08ms)
    // --------------------------------------------------------
    double diff = (P_new - P).norm();
    P = P_new;

    if (diff < params_.riccati_tol) {
      P_out = P;
      // 수렴한 P를 다음 스텝의 warm-start 초기값으로 저장
      P_prev_ = P;
      is_p_initialized_ = true;
      return true;  // 수렴 성공
    }
  }

  // --------------------------------------------------------
  // 최대 반복 횟수 초과 (false 반환)
  //
  //   이 경우에도 P를 Q로 대체하지 않고 현재 P를 사용함.
  //
  //   [왜 Q로 대체하면 안 되나?]
  //   P=Q로 fallback하면 K 행렬의 x,y,θ 열이 모두 0이 됨.
  //   → 위치/헤딩 오차에 무반응 → 로봇이 직진만 함.
  //   (이전 실험에서 Hamiltonian 방식 실패 시 이 문제가 발생했었음)
  //
  //   현재 P는 최대 iter만큼 수렴한 근사값으로,
  //   특히 warm-start 이후에는 P_prev ≈ P*이므로
  //   tol 미달이어도 제어에는 충분히 유효함.
  //
  //   경로 수신 직후 cold-start 시에만 주로 발생하며,
  //   해당 스텝 직후부터 warm-start로 전환되어 0.08ms로 복귀함.
  // --------------------------------------------------------
  P_out = P;
  // 미수렴 P도 다음 스텝의 더 나은 초기값으로 활용하기 위해 저장
  P_prev_ = P;
  is_p_initialized_ = true;
  return false;  // 수렴 실패 (하지만 제어는 정상 동작)
}

// ============================================================
// compute() — TVLQR 메인 계산 (매 50Hz 제어 주기마다 호출, 메인 파이프라인 함수)
//
// [TVLQR의 핵심 아이디어]
//   T = Time-Varying: 매 주기마다 선형화 동작점이 바뀜
//   → A_d, B_d가 매 주기 갱신 → K가 매 주기 갱신
//
//   일반 LQR(Offline):
//     한 번 고정된 동작점에서 K를 계산하고 영원히 사용
//     → 직선 구간에서만 좋음, 꺾임 구간에서 성능 저하
//
//   TVLQR(Online):
//     매 주기 현재 x_ref에서 A_d, B_d 재계산 → K 재계산
//     → 비선형 경로에서도 항상 최신 선형화 근사 적용
//     → 계산 부하는 증가하지만 warm-start로 실용적 수준 유지
//
// [처리 흐름 요약]
//   x_ref → 선형화(A_c,B_c) → 이산화(A_d,B_d) → Riccati(P)
//   → K 계산 → 오차 계산(e) → Δu = -K·e → 클리핑 → 반환
//
// [입력 인수]
//   x0    : 현재 로봇 상태 [x_pos, y_pos, θ, v, ω] (map frame)
//   x_ref : 현재 추종 목표 상태 [x_ref, y_ref, θ_ref, v_ref, 0]
//   u_prev: 직전 스텝에서 실제 발행된 속도 [v_prev, ω_prev]
//           (Δu → 절대 속도 변환에 사용)
// ============================================================
LqrSolution LqrCore::compute(
  const LqrStateVec & x0,
  const LqrStateVec & x_ref,
  const LqrInputVec & u_prev)
{
  LqrSolution sol;
  sol.success = false;
  sol.u0      = LqrInputVec::Zero();
  sol.K       = LqrGainMat::Zero();

  // --------------------------------------------------------
  // Step 1: 연속시간 Jacobian 선형화
  //
  //   x_ref를 동작점으로 사용:
  //   "우리가 지금 있어야 할 위치"에서의 시스템 특성을 기준으로
  //   선형화. 추종 오차가 줄수록 이 근사의 정확도가 높아짐.
  // --------------------------------------------------------
  LqrStateMat A_c;
  LqrInputMat B_c;
  linearize(x_ref, A_c, B_c);

  // --------------------------------------------------------
  // Step 2: 이산화 (Euler 1차)
  //
  //   A_c, B_c → A_d, B_d
  //   이후 모든 계산은 이산시간 모델 기준으로 진행
  // --------------------------------------------------------
  LqrStateMat A_d;
  LqrInputMat B_d;
  discretize(A_c, B_c, A_d, B_d);

  // --------------------------------------------------------
  // Step 3: Riccati 방정식 풀기 → 최적 비용 행렬 P
  //
  //   warm-start 덕분에 대부분의 경우 수 회만에 수렴.
  //   sol.success = false여도 P는 유효한 근사값이므로
  //   제어를 중단하지 않고 계속 진행함.
  // --------------------------------------------------------
  LqrStateMat P;
  bool converged = solveRiccati(A_d, B_d, P);
  sol.success    = converged;

  // --------------------------------------------------------
  // Step 4: 최적 게인 행렬 K 계산
  //
  //   K = (R + B^T P B)^{-1} · B^T P A    (2×5 행렬)
  //
  //   [K의 물리적 의미]
  //   K(0,:) = Δv를 결정하는 게인 벡터
  //   K(1,:) = Δω를 결정하는 게인 벡터
  //
  //   K(1,0), K(1,1): x, y 오차가 ω에 미치는 게인
  //   K(1,2):         θ 오차가 ω에 미치는 게인 (heading 보정)
  //
  //   [K와 Q/R의 관계]
  //   Q 크고 R 작음 → P 커짐 → K 커짐 → 오차에 강하게 반응
  //   Q 작고 R 큼   → P 작아짐 → K 작아짐 → 온순한 제어
  //
  //   [실험에서 관찰된 K 값 (θ=45°, v=0.1 기준)]
  //   K[0,:] ≈ [0.587, 0.587, 0,     0.328, 0    ]  Δv 게인
  //   K[1,:] ≈ [-0.783, 0.783, 0.839, 0,     0.404]  Δω 게인
  //   → K[1,0]=-0.783, K[1,1]=0.783: x오차와 y오차가 반대 방향으로 ω에 영향
  //   → K[1,2]=0.839: heading 오차가 ω에 가장 강하게 영향
  // S 행렬의 의미: S = R + Bd^T P Bd
  // R: 입력 자체에 대한 직접 패널티
  // Bd^T P Bd: 입력이 상태를 바꾸고, 그 상태가 미래 비용에 미치는 간접 영향
  // S가 크다: 입력 u를 쓰는 비용이 비싸다 = S^-1이 작아짐 -> K가 작아짐 -> 온순한 제어
  // --------------------------------------------------------
  Eigen::Matrix<double, LQR_NU, LQR_NX> BtPA = B_d.transpose() * P * A_d;
  Eigen::Matrix<double, LQR_NU, LQR_NU> S     = R_ + B_d.transpose() * P * B_d;
  LqrGainMat K = S.inverse() * BtPA;
  sol.K = K;

  // --------------------------------------------------------
  // Step 5: 추적 오차 계산  e = x0 - x_ref
  //
  //   e = [e_x, e_y, e_θ, e_v, e_ω]^T
  //
  //   e(0) = x0(0) - x_ref(0): x 위치 오차 [m]
  //   e(1) = x0(1) - x_ref(1): y 위치 오차 [m]
  //   e(2) = x0(2) - x_ref(2): heading 오차 [rad] ← 정규화 필요
  //   e(3) = x0(3) - x_ref(3): 선속도 오차 [m/s]
  //   e(4) = x0(4) - x_ref(4): 각속도 오차 [rad/s]
  //
  //   [왜 yaw 오차만 별도로 정규화하나?]
  //   x0(2)는 continuous yaw (누적값, ±π 경계 없음)
  //   x_ref(2)는 atan2로 계산된 raw heading [-π, π]
  //   단순 빼기 e(2) = x0(2) - x_ref(2)를 하면:
  //     예: x0(2) = 370° (1바퀴+10°), x_ref(2) = -10°
  //     → e(2) = 380° (실제 오차는 20°)
  //   atan2(sin, cos) 트릭으로 항상 [-π, π]로 정규화:
  //     sin(e)/cos(e) = sin(e mod 2π)/cos(e mod 2π)
  //     → 항상 올바른 방향으로 보정 명령이 나감
  // --------------------------------------------------------
  LqrStateVec e = x0 - x_ref;

  // yaw 오차만 별도 정규화: [-π, π] 범위로 강제
  double raw_yaw_err = e(2);
  e(2) = std::atan2(std::sin(raw_yaw_err), std::cos(raw_yaw_err));

  // --------------------------------------------------------
  // Step 6: 최적 제어 입력 계산
  //
  //   Δu = -K · e    (2×1 벡터)
  //
  //   [부호의 의미]
  //   e > 0: 현재 상태가 목표보다 크다 → Δu < 0 (줄이는 방향)
  //   e < 0: 현재 상태가 목표보다 작다 → Δu > 0 (늘리는 방향)
  //
  //   [LQR 최적성]
  //   이 Δu = -K·e가 무한 구간 비용 J = Σ(e^T Q e + u^T R u)를
  //   최소화하는 이론적으로 최적인 제어 입력임.
  //   (단, 시스템이 정확히 선형이고 Q,R이 적절히 설정된 경우)
  //
  //   Δu(0) = Δv: 선속도 변화량 [m/s]
  //   Δu(1) = Δω: 각속도 변화량 [rad/s]
  // --------------------------------------------------------
  LqrInputVec delta_u = -K * e;

  // --------------------------------------------------------
  // Step 7: 속도 클리핑 (물리적 제한 강제 적용)
  //
  //   [LQR의 constraint 처리 한계]
  //   MPC는 QP 풀이 과정에서 v_max, ω_max 등을 hard constraint로
  //   처리할 수 있음. LQR은 이 기능이 없어 사후에 클리핑으로 처리.
  //   → 클리핑이 자주 발생하면 최적성이 깨짐
  //   → 이 실험에서 |ω|=1.0 클리핑이 3.1% 발생 (허용 범위)
  //
  //   [u_prev를 쓰는 이유]
  //   LQR의 입력 정의:
  //     u = [Δv, Δω] (속도 변화량)
  //     실제 속도 = u_prev + Δu
  //
  //   즉, compute()는 Δu를 반환하고
  //   lqr_node.cpp에서 v_cmd = u_prev(0) + sol.u0(0)으로 최종 속도를 구함.
  //
  //   클리핑을 Δu가 아닌 최종 속도(v_cmd)에 적용하는 이유:
  //   Δu만 클리핑하면 u_prev가 이미 v_max에 달해있을 때
  //   실제 속도가 v_max를 초과할 수 있음.
  // --------------------------------------------------------
  double v_cmd = std::clamp(
    u_prev(0) + delta_u(0),
    params_.v_min, params_.v_max);

  double w_cmd = std::clamp(
    u_prev(1) + delta_u(1),
    params_.w_min, params_.w_max);

  // 클리핑 후 실제로 적용 가능한 Δu를 역산하여 반환
  // lqr_node.cpp에서 v_cmd = u_prev(0) + sol.u0(0) 으로 다시 계산함
  sol.u0(0) = v_cmd - u_prev(0);  // 실제 적용 가능한 Δv
  sol.u0(1) = w_cmd - u_prev(1);  // 실제 적용 가능한 Δω

  return sol;
}

